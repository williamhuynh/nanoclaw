// --- updateChatName ---

describe('updateChatName', () => {
  it('updates name of existing chat while preserving the original timestamp', () => {
    const originalTimestamp = '2024-06-15T10:00:00.000Z';
    storeChatMetadata('group@g.us', originalTimestamp, 'Old Name');

    updateChatName('group@g.us', 'New Name');

    const chats = getAllChats();
    expect(chats).toHaveLength(1);
    expect(chats[0].name).toBe('New Name');
    // The original timestamp should be preserved (ON CONFLICT updates name only)
    expect(chats[0].last_message_time).toBe(originalTimestamp);
  });

  it('creates a new chat entry if JID does not exist', () => {
    updateChatName('new@g.us', 'Brand New Chat');

    const chats = getAllChats();
    const chat = chats.find((c) => c.jid === 'new@g.us');
    expect(chat).toBeDefined();
    expect(chat!.name).toBe('Brand New Chat');
  });
});

// --- setSession + getSession round-trip ---

describe('setSession + getSession', () => {
  it('round-trips a session ID', () => {
    setSession('group-a', 'session-xyz');
    expect(getSession('group-a')).toBe('session-xyz');
  });

  it('overwrites a session when set again', () => {
    setSession('group-b', 'old-session');
    setSession('group-b', 'new-session');
    expect(getSession('group-b')).toBe('new-session');
  });

  it('returns undefined for a group with no session', () => {
    expect(getSession('no-such-group')).toBeUndefined();
  });
});

// --- getAllSessions ---

describe('getAllSessions', () => {
  it('returns an empty object when no sessions exist', () => {
    expect(getAllSessions()).toEqual({});
  });

  it('returns correct map after setting multiple sessions', () => {
    setSession('folder-1', 'sess-1');
    setSession('folder-2', 'sess-2');
    setSession('folder-3', 'sess-3');

    const sessions = getAllSessions();
    expect(sessions).toEqual({
      'folder-1': 'sess-1',
      'folder-2': 'sess-2',
      'folder-3': 'sess-3',
    });
  });

  it('reflects deletions', () => {
    setSession('folder-a', 'sess-a');
    setSession('folder-b', 'sess-b');
    deleteSession('folder-a');

    const sessions = getAllSessions();
    expect(sessions).toEqual({
      'folder-b': 'sess-b',
    });
  });
});
